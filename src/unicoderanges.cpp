//----------------------------------------------------------------------------------------------------------------------
UnicodeRange UnicodeRanges::getRange(const QString &name)
{
//    UnicodeRange range;
    for(int i = 0; i < unicodesRanges.size(); ++i)
    {
        auto &range = unicodesRanges[i];
        if(range.name == name)
        {
            return range;
        }
    }
    return UnicodeRange();
}

